
public class Colorrep {
	private int selfred; 
	private int selfblue;
	private int selfgreen;
	
	public int getred(){return this.selfred;}
	public int getgreen(){return this.selfgreen;}
	public int getblue(){return this.selfblue;}
	
	public Colorrep (int r, int g, int b){
		selfred = r;
		selfgreen = g;
		selfblue = b;
		
	}
}
